package com.itrainu.Basic;

public class logicaloprators {

	public static void main(String[] args) {

		// if both condition is true only then output is true otherwise is false
		System.out.println(64 > 66 && 10 == 10);

		// ( || ) means "OR" dono mAI se koi ek true ho jaye to true output aa jata hai

		System.out.println(2 < 2 || 22 == 22);
	}

}
